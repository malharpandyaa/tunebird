// ── Cookie helpers ────────────────────────────────────────────────────────────
function cookieFileExists() {
  try { return fs.existsSync(COOKIE_FILE) && fs.statSync(COOKIE_FILE).size > 100; }
  catch { return false; }
}

function getCookieArgs() {
  return cookieFileExists() ? ['--cookies', COOKIE_FILE] : [];
}

// Convert Electron cookies to Netscape format for yt-dlp
async function saveElectronCookies(sess) {
  const cookies = await sess.cookies.get({ domain: '.youtube.com' });
  const musicCookies = await sess.cookies.get({ domain: '.music.youtube.com' });
  const allCookies = [...cookies, ...musicCookies];

  if (!allCookies.length) return false;

  const lines = ['# Netscape HTTP Cookie File', '# Generated by Tuneplug', ''];
  for (const c of allCookies) {
    const domain     = c.domain.startsWith('.') ? c.domain : `.${c.domain}`;
    const subDomain  = domain.startsWith('.') ? 'TRUE' : 'FALSE';
    const secure     = c.secure ? 'TRUE' : 'FALSE';
    const expiry     = c.expirationDate ? Math.floor(c.expirationDate) : 0;
    lines.push(`${domain}\t${subDomain}\t${c.path || '/'}\t${secure}\t${expiry}\t${c.name}\t${c.value}`);
  }

  fs.writeFileSync(COOKIE_FILE, lines.join('\n'));
  return true;
}

// ── Binary detection ──────────────────────────────────────────────────────────
function findBinary(name) {
  for (const p of [
    `/opt/homebrew/bin/${name}`,
    `/usr/local/bin/${name}`,
    `/usr/bin/${name}`,
    path.join(app.getPath('userData'), 'bin', name),
  ]) { try { if (fs.existsSync(p)) return p; } catch {} }
  return name;
}

