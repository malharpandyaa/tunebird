// ── Login: open YouTube Music in a popup window ───────────────────────────────
ipcMain.handle('login', async () => {
  return new Promise((resolve) => {
    if (loginWindow) { loginWindow.focus(); resolve({ success: false }); return; }

    // Use a separate session so cookies are isolated
    const loginSess = session.fromPartition('persist:youtube-login');

    loginWindow = new BrowserWindow({
      width: 500, height: 680,
      title: 'Sign in to YouTube Music',
      parent: mainWindow,
      modal: true,
      webPreferences: {
        session: loginSess,
        nodeIntegration: false,
        contextIsolation: true,
      },
    });

    loginWindow.loadURL('https://accounts.google.com/ServiceLogin?service=youtube&uilel=3&passive=true&continue=https%3A%2F%2Fmusic.youtube.com%2F');

    // Poll for successful login by checking for YouTube Music cookies
    const checkInterval = setInterval(async () => {
      try {
        const cookies = await loginSess.cookies.get({ name: 'SAPISID' });
        if (cookies.length > 0) {
          // Logged in — save cookies and close
          clearInterval(checkInterval);
          const saved = await saveElectronCookies(loginSess);
          if (saved) track('user_signed_in');
          loginWindow.close();
          resolve({ success: saved });
        }
      } catch {}
    }, 1000);

    loginWindow.on('closed', () => {
      clearInterval(checkInterval);
      loginWindow = null;
      resolve({ success: cookieFileExists() });
    });
  });
});

